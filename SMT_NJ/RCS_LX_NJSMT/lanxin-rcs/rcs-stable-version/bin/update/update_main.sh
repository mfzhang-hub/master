#!/bin/bash

RCS_SERVER_USER=$1
RCS_SERVER_PASSWD=$2
RCS_PACKAGE_FILE=$3
RCS_PROJECT_VERSION=$4

if [ -z "${RCS_SERVER_USER}" ]; then
  RCS_SERVER_USER=lanxin
fi
if [ -z "${RCS_SERVER_PASSWD}" ]; then
  RCS_SERVER_PASSWD=admin123
fi

RCS_SERVER_HOME=/home/${RCS_SERVER_USER}
SCRIPT_DIR=${RCS_SERVER_HOME}/upgrade/update

nohup sh ${SCRIPT_DIR}/update_rcs.sh $RCS_SERVER_USER $RCS_SERVER_PASSWD $RCS_PACKAGE_FILE $RCS_PROJECT_VERSION > ${RCS_SERVER_HOME}/update_rcs.log 2>&1 &